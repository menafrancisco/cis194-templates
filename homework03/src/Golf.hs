-- >>> skips "ABCD"
-- ["ABCD","BD","C","D"]
-- >>> skips "hello!"
-- ["hello!","el!","l!","l","o","!"]
-- >>> skips [1]
-- [[1]]
-- >>> skips [True, False]
-- [[True,False],[False]]
-- >>> skips []
-- []

f ::[a] -> Int-> [a]
f   []   _ = []
f (x:xs) n = x: (f $ drop m xs) n
    where m = n - 1

skips :: [a] -> [[a]]
skips = skips' 0


skips' ::Int -> [a] -> [[a]]
skips' _ [] = []
skips' n l@(_:xs) =  f l m : skips' m xs
    where m =n+1

